class SegFusionVAN(nn.Module):
    def __init__(self, pretrained=False, in_chans=3, num_classes=5):
        super().__init__()

        # unet pretrained model path
        model_path = r'./saved_unet_model/best_unet_model.pth'
        self.unet = UNet(in_channels=in_chans, out_channels=in_chans).to(config.device)
        # load model parameters
        self.unet.load_state_dict(torch.load(model_path))
        self.unet.eval()

        self.net = van_b0(pretrained=pretrained, in_chans=in_chans, num_classes=num_classes)

        self.w = [
            nn.Parameter(torch.rand([56 // 2 ** i, 56 // 2 ** i])).to(config.device) for i in
            range(self.net.num_stages)]
        self.b = [
            nn.Parameter(torch.rand([56 // 2 ** i, 56 // 2 ** i])).to(config.device) for i in
            range(self.net.num_stages)]
        self.bn = [nn.BatchNorm2d(self.net.embed_dims[i]).to(config.device) for i in range(self.net.num_stages)]
        self.gap = [nn.AdaptiveAvgPool2d(1) for _ in range(self.net.num_stages + 1)]
        # self.gcn = GCNNet(num_classes=num_classes, out_channel=np.sum(self.net.embed_dims) + self.net.embed_dims[-1],
        #                   t=0.3)
        self.fc = nn.Linear(in_features=np.sum(self.net.embed_dims) + self.net.embed_dims[-1], out_features=num_classes)

    def forward(self, x):
        # seg
        with torch.no_grad():
            pred = self.unet(x).detach()
            seg_mask = torch.argmax(pred, dim=1).unsqueeze(1)
            seg_mask[seg_mask > 0] = 1

        seg_mask = seg_mask.to(config.device)

        feature_maps = self.net(x)

        result = []
        for i, m in enumerate(feature_maps):
            m_size = m.shape[-2:]
            seg_mask_resize = transforms.Resize(m_size, antialias=True)(seg_mask)
            result.append(self.gap[i](self.bn[i]((self.w[i] * m + self.b[i]) * seg_mask_resize)).flatten(1))

        result.append(self.gap[-1](feature_maps[-1]).flatten(1))
        result = torch.cat(result, dim=1)

        out = self.fc(result)

        # y = self.gcn()
        # out = torch.matmul(result, y)

        return out


