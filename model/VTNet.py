class MyVTNet(nn.Module):
    def __init__(self, pretrained=False, img_size=224, in_chans=3, num_classes=5):
        super().__init__()

        self.backbone = van_b0(pretrained=pretrained, in_chans=in_chans, num_classes=num_classes)
        self.transformer = Transformer(num_encoder_layers=3)

        hidden_dim = self.transformer.d_model
        self.pos_emb = PositionEmbeddingSine(num_pos_feats=hidden_dim // 2, normalize=True, maxH=img_size // 32,
                                             maxW=img_size // 32)
        self.input_proj = nn.Conv2d(self.backbone.embed_dims[-1], hidden_dim, kernel_size=1)
        self.query_embed = nn.Embedding(num_classes, hidden_dim)
        self.fc = GroupWiseLinear(num_classes, hidden_dim, bias=True)

        self.W1 = nn.Parameter(torch.Tensor(1, num_classes))
        self.W2 = nn.Parameter(torch.Tensor(1, num_classes))
        self.b = nn.Parameter(torch.Tensor(1, num_classes))
        nn.init.constant_(self.W1, 0.5)
        nn.init.constant_(self.W2, 0.5)
        nn.init.constant_(self.b, 0)

    def forward(self, input):
        src, pred1 = self.backbone(input)
        pos = self.pos_emb(src)
        query_input = self.query_embed.weight
        hs = self.transformer(self.input_proj(src), query_input, pos)[0]  # B,K,d
        pred2 = self.fc(hs[-1])

        out = self.W1 * pred1 + self.W2 * pred2 + self.b

        return out


